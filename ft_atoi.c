#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int res;
	char *t;

	t = (char *)str;
	i = 0;
	res = 0;
	if (t[0] == '-')
		i++;
	while (t[i])
	{
		if (t[i] < '0' || t[i] > '9')
			t[i] = '\0';
		else
		{
			res = res * 10 + (t[i] - 48);
			i++;
		}
	}
	if (t[0] == '-')
		res = -res;
	return (res);
}
