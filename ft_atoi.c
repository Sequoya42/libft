#include <stdlib.h>

int		ft_atoi(char *str)
{
	int i;
	int res;

	i = 0;
	res = 0;
	if (str[0] == '-')
		i++;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			str[i] = '\0';
		else
		{
			res = res * 10 + (str[i] - 48);
			i++;
		}
	}
	if (str[0] == '-')
		res = -res;
	return (res);
}
