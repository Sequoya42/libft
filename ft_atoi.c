#include <stdlib.h>

int		ft_atoi(const char *str)
{
	int i;
	int res;
	char *s;

	i = 0;
	s = (char *)str;
	res = 0;
	if (s[0] == '-')
		i++;
	while (s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			s[i] = '\0';
		else
		{
			res = res * 10 + (s[i] - 48);
			i++;
		}
	}
	if (s[0] == '-')
		res = -res;
	return (res);
}
